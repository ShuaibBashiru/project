<h2>Symptoms</h2>
<p>Signs and symptoms of coronavirus disease 2019 (COVID-19) may appear two to 14 days after exposure. This time after exposure and before having symptoms is called the incubation period. Common signs and symptoms can include:</p>
<ul>
<li>Fever</li>
<li>Cough</li>
<li>Tiredness</li>
</ul>
<p>Early symptoms of <abbr title="coronavirus disease 2019">COVID-19</abbr> may include a loss of taste or smell.</p>
<p>Other symptoms can include:</p>
<ul>
<li>Shortness of breath or difficulty breathing</li>
<li>Muscle aches</li>
<li>Chills</li>
<li>Sore throat</li>
<li>Runny nose</li>
<li>Headache</li>
<li>Chest pain</li>
<li>Pink eye (conjunctivitis)</li>
</ul>
<p>This list is not all inclusive. Other less common symptoms have been reported, such as rash, nausea, vomiting and diarrhea. Children have similar symptoms to adults and generally have mild illness.</p>
<p>The severity of <abbr title="coronavirus disease 2019">COVID-19</abbr> symptoms can range from very mild to severe. Some people may have only a few symptoms, and some people may have no symptoms at all. Some people may experience worsened symptoms, such as worsened shortness of breath and pneumonia, about a week after symptoms start.</p>
<p>People who are older have a higher risk of serious illness from <abbr title="coronavirus disease 2019">COVID-19</abbr>, and the risk increases with age. People who have existing chronic medical conditions also may have a higher risk of serious illness. Certain medical conditions that increase the risk of serious illness from <abbr title="coronavirus disease 2019">COVID-19</abbr> include:</p>
<ul>
<li>Serious heart diseases, such as heart failure, coronary artery disease or cardiomyopathy</li>
<li>Cancer</li>
<li>Chronic obstructive pulmonary disease (COPD)</li>
<li>Type 2 diabetes</li>
<li>Obesity or severe obesity</li>
<li>Smoking</li>
<li>Chronic kidney disease</li>
<li>Sickle cell disease</li>
<li>Weakened immune system from solid organ transplants</li>
<li>Pregnancy</li>
</ul>
<p>Other conditions may increase the risk of serious illness, such as:</p>
<ul>
<li>Asthma</li>
<li>Liver disease</li>
<li>Overweight</li>
<li>Chronic lung diseases such as cystic fibrosis or pulmonary fibrosis</li>
<li>Brain and nervous system conditions</li>
<li>Weakened immune system from bone marrow transplant, HIV or some medications</li>
<li>Type 1 diabetes</li>
<li>High blood pressure</li>
</ul>
<p>This list is not all inclusive. Other underlying medical conditions may increase your risk of serious illness from <abbr title="coronavirus disease 2019">COVID-19</abbr>.</p>
