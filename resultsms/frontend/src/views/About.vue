<h3>When to see a doctor</h3>
<p>If you have <abbr title="coronavirus disease 2019">COVID-19</abbr> symptoms or you've been in contact with someone diagnosed with <abbr title="coronavirus disease 2019">COVID-19</abbr>, contact your doctor or clinic right away for medical advice. Tell your health care team about your symptoms and possible exposure before you go to your appointment.</p>
<p>If you have emergency <abbr title="coronavirus disease 2019">COVID-19</abbr> signs and symptoms, seek care immediately. Emergency signs and symptoms can include:</p>
<ul>
<li>Trouble breathing</li>
<li>Persistent chest pain or pressure</li>
<li>Inability to stay awake</li>
<li>New confusion</li>
<li>Blue lips or face</li>
</ul>
<p>If you have signs or symptoms of <abbr title="coronavirus disease 2019">COVID-19</abbr>, contact your doctor or clinic for guidance. Let your doctor know if you have other chronic medical conditions, such as heart disease or lung disease. During the pandemic, it's important to make sure health care is available for those in greatest need.</p>
