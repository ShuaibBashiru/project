<h2>Prevention</h2>
<p>The U.S. Food and Drug Administration (FDA) has given emergency use authorization for two <abbr title="coronavirus disease 2019">COVID-19</abbr> vaccines, the Pfizer/BioNTech <abbr title="coronavirus disease 2019">COVID-19</abbr> vaccine and the Moderna <abbr title="coronavirus disease 2019">COVID-19</abbr> vaccine. A vaccine might prevent you from getting <abbr title="coronavirus disease 2019">COVID-19</abbr> or prevent you from becoming seriously ill from <abbr title="coronavirus disease 2019">COVID-19</abbr> if you get the <abbr title="coronavirus disease 2019">COVID-19</abbr> virus. You can take additional steps to reduce your risk of infection. <abbr title="World Health Organization">WHO</abbr> and <abbr title="Centers for Disease Control and Prevention">CDC</abbr> recommend following these precautions for avoiding exposure to the virus that causes <abbr title="coronavirus disease 2019">COVID-19</abbr>:</p>
<ul>
<li>Avoid close contact (within about 6 feet, or 2 meters) with anyone who is sick or has symptoms.</li>
<li>Keep distance between yourself and others (within about 6 feet, or 2 meters). This is especially important if you have a higher risk of serious illness. Keep in mind some people may have <abbr title="coronavirus disease 2019">COVID-19</abbr> and spread it to others, even if they don't have symptoms or don't know they have <abbr title="coronavirus disease 2019">COVID-19</abbr>.</li>
<li>Wash your hands often with soap and water for at least 20 seconds, or use an alcohol-based hand sanitizer that contains at least 60% alcohol.</li>
<li>Cover your face with a cloth face mask in public spaces, such as the grocery store, where it's difficult to avoid close contact with others. Surgical masks may be used if available. N95 respirators should be reserved for health care providers.</li>
<li>Cover your mouth and nose with your elbow or a tissue when you cough or sneeze. Throw away the used tissue. Wash your hands right away.</li>
<li>Avoid touching your eyes, nose and mouth.</li>
<li>Avoid sharing dishes, glasses, towels, bedding and other household items if you're sick.</li>
<li>Clean and disinfect high-touch surfaces, such as doorknobs, light switches, electronics and counters, daily.</li>
<li>Stay home from work, school and public areas if you're sick, unless you're going to get medical care. Avoid public transportation, taxis and ride-sharing if you're sick.</li>
</ul>
<p>If you have a chronic medical condition and may have a higher risk of serious illness, check with your doctor about other ways to protect yourself.</p>
