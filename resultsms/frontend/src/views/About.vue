<h2>Causes</h2>
<p>Infection with the new coronavirus (severe acute respiratory syndrome coronavirus 2, or SARS-CoV-2) causes coronavirus disease 2019 (<abbr title="coronavirus disease 2019">COVID-19</abbr>).</p>
<p>The virus that causes <abbr title="coronavirus disease 2019">COVID-19</abbr> spreads easily among people, and more continues to be discovered over time about how it spreads. Data has shown that it spreads mainly from person to person among those in close contact (within about 6 feet, or 2 meters). The virus spreads by respiratory droplets released when someone with the virus coughs, sneezes, breathes, sings or talks. These droplets can be inhaled or land in the mouth, nose or eyes of a person nearby.</p>
<p>In some situations, the <abbr title="coronavirus disease 2019">COVID-19</abbr> virus can spread by a person being exposed to small droplets or aerosols that stay in the air for several minutes or hours &mdash; called airborne transmission. It's not yet known how common it is for the virus to spread this way.</p>
<p>It can also spread if a person touches a surface or object with the virus on it and then touches his or her mouth, nose or eyes, although this isn't considered to be a main way it spreads.</p>
<p>Some reinfections of the virus that causes <abbr title="coronavirus disease 2019">COVID-19</abbr> have happened, but these have been uncommon.</p>
<h2>Risk factors</h2>
<p>Risk factors for <abbr title="coronavirus disease 2019">COVID-19</abbr> appear to include:</p>
<ul>
<li>Close contact (within 6 feet, or 2 meters) with someone who has <abbr title="coronavirus disease 2019">COVID-19</abbr></li>
<li>Being coughed or sneezed on by an infected person</li>
</ul>
<h2>Complications</h2>
<p>Although most people with <abbr title="coronavirus disease 2019">COVID-19</abbr> have mild to moderate symptoms, the disease can cause severe medical complications and lead to death in some people. Older adults or people with existing chronic medical conditions are at greater risk of becoming seriously ill with <abbr title="coronavirus disease 2019">COVID-19</abbr>.</p>
<p>Complications can include:</p>
<ul>
<li>Pneumonia and trouble breathing</li>
<li>Organ failure in several organs</li>
<li>Heart problems</li>
<li>A severe lung condition that causes a low amount of oxygen to go through your bloodstream to your organs (acute respiratory distress syndrome)</li>
<li>Blood clots</li>
<li>Acute kidney injury</li>
<li>Additional viral and bacterial infections</li>
</ul>
